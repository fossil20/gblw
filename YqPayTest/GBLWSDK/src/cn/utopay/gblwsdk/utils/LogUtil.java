package cn.utopay.gblwsdk.utils;

import android.util.Log;

public class LogUtil {
	public static boolean CANPRINT = true;

	public static void v(String log) {
		if (CANPRINT) {
			Log.v("GBLWPAY", log);
		}
	}

	public static void e(Exception e) {
		if (CANPRINT) {
			Log.e("GBLWPAY", "", e);
		}
	}
}
